package model;

public class rap {
	private String tenRap;
	private int soCho;
	
	public rap() {
		
	}
	
	public rap(String tenRap, int soCho) {
		this.tenRap = tenRap;
		this.soCho = soCho;
	}

	public String getTenRap() {
		return tenRap;
	}

	public void setTenRap(String tenRap) {
		this.tenRap = tenRap;
	}

	public int getSoCho() {
		return soCho;
	}

	public void setSoCho(int soCho) {
		this.soCho = soCho;
	}
	
	
}
